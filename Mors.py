def mors_to_text(translate):

    MorsIn = str(input("Enter mors code:"))
    MorsSep = MorsIn.split('/')
    
    for x in MorsSep:
        y = x.split(' ')
        for z in y:
            if z != "":
                translate += Mors.get(z)
                    
        translate += " "

    print(translate)


